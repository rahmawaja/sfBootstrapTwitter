	/**
	 * the index action
	 * ================
	 * 
	 * @param sfWebRequest $request
	 */
	public function executeIndex(sfWebRequest $request) {
		if($request->hasParameter('q')) {
			$this->forward($this->getModuleName(), 'autocomplete');
		}
		
		$this->form = new sfHtml5FormDemoForm();
		
		
		if($request->getMethod() == 'POST') {
			if($request->hasParameter($this->form->getName())) {
				$this->form->bind(
					$request->getParameter($this->form->getName())
				);
				if($this->form->isValid()) {
					$this->getUser()->setFlash('ok', 'Form is valid.');
				} else {
					$this->getUser()->setFlash('error', 'Form is invalid.');
				}
			}
		}	
	}
	
	
	/**
	 * the autocomplete action
	 * =======================
	 * 
	 * called by the sfWidgetFormInputDatalist in autocompletion mode
	 * 
	 * @param sfWebRequest $request
	 */
	public function executeAutocomplete(sfWebRequest $request) {
		if($request->getParameter('q', false)) {
			$this->query = $request->getParameter('q');
			// filter the data for strings containing the query
			$this->data = array_filter(self::$autocompleteData, array($this, 'filterCallback'));
		} else {
			$this->data = array();
		}
		
		$this->response->setHttpHeader('Content-Type', 'application/xml', true);
		
		$this->setLayout(false);
		sfConfig::set('sf_web_debug', false);
		
	}
	
	/**
	 * the callback function to filter for matching strings
	 * (called by array_filter())
	 * 
	 * @see http://php.net/manual/en/function.array-filter.php
	 * @param $value     the value to check if the query is part of it
	 * @return boolean   true if $value contains $this->query
	 */
	protected function filterCallback($value) {
		return stripos($value, $this->query) !== false;
	}
	
